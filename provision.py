def parse_form(body):
    """Parse URL-encoded form data"""
    params = {}
    for pair in body.split('&'):
        if '=' in pair:
            key, val = pair.split('=', 1)
            # URL decode
            val = val.replace('+', ' ')
            val = val.replace('%21', '!').replace('%40', '@')
            val = val.replace('%23', '#').replace('%24', '$')
            val = val.replace('%25', '%').replace('%26', '&')
            val = val.replace('%3D', '=').replace('%3F', '?')
            params[key] = val
    return params


def start_ap():
    """Start the access point"""
    ap = network.WLAN(network.AP_IF)
    ap.active(True)
    ap.config(essid=AP_SSID, password=AP_PASSWORD)

    # Wait for AP to be active
    while not ap.active():
        time.sleep(0.1)

    print(f"AP started: {AP_SSID}")
    print(f"AP password: {AP_PASSWORD}")
    print(f"IP: {ap.ifconfig()[0]}")
    return ap


def _dns_server(ip):
    """Tiny DNS server that redirects all queries to the AP IP.
    This triggers the captive portal popup on phones."""
    import struct

    sock = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
    sock.setsockopt(socket.SOL_SOCKET, socket.SO_REUSEADDR, 1)
    sock.bind(('0.0.0.0', 53))

    # Pack AP IP as 4 bytes
    ip_bytes = bytes(int(b) for b in ip.split('.'))

    print(f"DNS server running, redirecting all to {ip}")

    while True:
        try:
            data, addr = sock.recvfrom(512)
            # Build minimal DNS response: same ID, response flag, 1 answer
            # Copy the question from the request
            response = bytearray(data[:2])       # Transaction ID
            response += b'\x81\x80'              # Flags: response, no error
            response += data[4:6]                # Questions count
            response += b'\x00\x01'              # Answers count: 1
            response += b'\x00\x00\x00\x00'      # Authority + Additional: 0
            # Copy question section
            q_end = 12
            while data[q_end] != 0:
                q_end += data[q_end] + 1
            q_end += 5  # null byte + qtype(2) + qclass(2)
            response += data[12:q_end]
            # Answer: pointer to name, type A, class IN, TTL 60, IP
            response += b'\xc0\x0c'              # Name pointer to question
            response += b'\x00\x01'              # Type A
            response += b'\x00\x01'              # Class IN
            response += b'\x00\x00\x00\x3c'      # TTL 60
            response += b'\x00\x04'              # Data length 4
            response += ip_bytes                 # IP address
            sock.sendto(bytes(response), addr)
        except Exception as e:
            print(f"DNS error: {e}")


def run_server():
    """Run the provisioning captive portal with DNS redirect"""
    from machine import Pin
    import _thread

    led = Pin('LED', Pin.OUT)

    ap = start_ap()
    ip = ap.ifconfig()[0]

    # Start DNS server in background thread to redirect all domains to AP IP
    _thread.start_new_thread(_dns_server, (ip,))

    server = socket.socket()
    server.setsockopt(socket.SOL_SOCKET, socket.SO_REUSEADDR, 1)
    server.bind(('0.0.0.0', 80))
    server.listen(1)

    print(f"Provisioning server at http://{ip}")
    print("DNS redirect active — captive portal should auto-open")

    # Blink LED rapidly to indicate setup mode
    led.on()

    while True:
        try:
            conn, addr = server.accept()
            request = conn.recv(1024).decode()

            if 'POST /save' in request:
                # Extract body
                body = request.split('\r\n\r\n', 1)[-1]
                params = parse_form(body)

                ssid = params.get('ssid', '')
                password = params.get('password', '')

                if ssid:
                    # Send success page
                    response = SUCCESS_PAGE.format(ssid=ssid)
                    conn.send("HTTP/1.1 200 OK\r\nContent-Type: text/html\r\n\r\n")
                    conn.send(response)
                    conn.close()

                    # Save and reboot
                    save_config(ssid, password)
                    time.sleep(2)

                    ap.active(False)
                    import machine
                    machine.reset()
                else:
                    conn.send("HTTP/1.1 400 Bad Request\r\n\r\nMissing SSID")
                    conn.close()
            else:
                # Serve setup page for any GET request (captive portal)
                conn.send("HTTP/1.1 200 OK\r\nContent-Type: text/html\r\n\r\n")
                conn.send(SETUP_PAGE)
                conn.close()

        except Exception as e:
            print(f"Server error: {e}")
            try:
                conn.close()
            except:
                pass
